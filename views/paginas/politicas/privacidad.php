        <p><strong>Última actualización:</strong> 12 de julio de 2025</p>

        <p>Bienvenido a MixtliArte. Tu privacidad es de suma importancia para nosotros. Esta Política de Privacidad explica qué datos personales recopilamos, cómo los usamos, compartimos y protegemos.</p>

        <h3>1. Información que Recopilamos</h3>
        <p>Recopilamos diferentes tipos de información para proporcionar y mejorar nuestro servicio:</p>
        <ul>
            <li><strong>Información de Identificación Personal:</strong> Nombre, apellido, fecha de nacimiento, sexo, correo electrónico, y contraseña hasheada.</li>
            <li><strong>Información de Contacto:</strong> Número de teléfono y direcciones (residencial y/o comercial) que nos proporcionas.</li>
            <li><strong>Contenido Generado por el Usuario:</strong> Fotos de perfil, biografías, descripciones de productos, mensajes enviados a través de nuestro chat, y valoraciones o comentarios que dejas en la plataforma.</li>
            <li><strong>Datos de Transacciones:</strong> Llevamos un registro de los productos que marcas como "vendidos" para gestionar el sistema de valoraciones entre usuarios.</li>
            <li><strong>Datos de Interacción y Uso:</strong> Recopilamos información sobre cómo interactúas con MixtliArte. Esto incluye los productos que ves (clics), los que guardas en favoritos, tus búsquedas, el tiempo que pasas en las páginas de los productos y tus categorías de preferencia.</li>
            <li><strong>Información de Seguridad:</strong> Para proteger tu cuenta, almacenamos información relacionada con la autenticación de dos factores (2FA), si decides activarla.</li>
        </ul>

        <h3>2. ¿Cómo Usamos tu Información?</h3>
        <p>Utilizamos la información que recopilamos para varios propósitos:</p>
        <ul>
            <li><strong>Para proveer y mantener nuestro servicio:</strong> Gestionar tu cuenta, procesar tus acciones como comprador o vendedor y permitir la comunicación entre usuarios.</li>
            <li><strong>Para personalizar tu experiencia:</strong> Usamos tu historial de interacciones y preferencias para recomendarte productos y categorías que podrían interesarte.</li>
            <li><strong>Para mejorar la plataforma:</strong> Analizamos el comportamiento del usuario para mejorar la funcionalidad, identificar problemas y optimizar la experiencia.</li>
            <li><strong>Para comunicarnos contigo:</strong> Enviarte notificaciones sobre tu cuenta, mensajes, transacciones, cambios en nuestras políticas o con fines de soporte.</li>
            <li><strong>Para seguridad y moderación:</strong> Investigar reportes, aplicar sanciones y proteger a nuestra comunidad contra fraudes y abusos.</li>
        </ul>

        <h3>3. ¿Cómo Compartimos tu Información?</h3>
        <p>Tu privacidad es fundamental. No vendemos tus datos personales a terceros. La información se comparte únicamente en los siguientes casos:</p>
        <ul>
            <li><strong>Entre Usuarios para Transacciones:</strong> Para facilitar una compra-venta, tu nombre de perfil y los mensajes que intercambies son visibles para el otro usuario en el chat.</li>
            <li><strong>Perfil Público:</strong> Otros usuarios pueden ver la información de tu perfil público, como tu nombre, imagen, biografía (si eres vendedor) y las valoraciones que has recibido.</li>
            <li><strong>Requerimientos Legales:</strong> Podríamos divulgar tu información si así lo exige la ley.</li>
        </ul>

        <h3>4. Tus Derechos y Control sobre tus Datos</h3>
        <p>Te ofrecemos control sobre tu información personal:</p>
        <ul>
            <li><strong>Acceso y Portabilidad:</strong> Puedes solicitar una copia de todos tus datos en un formato JSON descargable desde tu perfil.</li>
            <li><strong>Rectificación:</strong> Puedes editar la información de tu perfil, incluyendo tus datos personales, direcciones y preferencias, en cualquier momento.</li>
            <li><strong>Eliminación ("Derecho al Olvido"):</strong> Puedes solicitar la eliminación permanente de tu cuenta y todos los datos asociados. Este proceso requiere una confirmación por correo electrónico y es irreversible.</li>
        </ul>

        <h3>5. Seguridad de los Datos</h3>
        <p>Nos comprometemos a proteger tu información. Utilizamos medidas de seguridad como el hasheo de contraseñas y ofrecemos autenticación de dos factores (2FA) para proteger tu cuenta contra accesos no autorizados.</p>

        <h3>6. Cambios a esta Política</h3>
        <p>Podemos actualizar nuestra Política de Privacidad ocasionalmente. Te notificaremos sobre cualquier cambio publicando la nueva política en esta página y, si los cambios son significativos, a través de una notificación por correo electrónico.</p>

        <h3>7. Contacto</h3>
        <p>Si tienes alguna pregunta sobre esta Política de Privacidad, no dudes en contactarnos a través de nuestra sección de <a href="/contacto">Contacto</a>.</p>
